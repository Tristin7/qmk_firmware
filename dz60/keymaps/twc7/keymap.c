uint32_t layer_state_set_user(uint32_t state) {
    switch (biton32(state)) {
    case Base:
        rgblight_enable();
        break;
    case System:
        rgblight_setrgb (51,  153, 255);
        break;
    case RGB:
        rgblight_enable();
        break;
    case Game_Mode:
        rgblight_setrgb (204,  51, 0);
        break;
    case KC_CAPSLOCK:
        rgblight_setrgb(255,204,0);
        break;
    default: //  for any other layers, or the default layer
        rgblight_enable();
        break;
    }

  return state;
}